"""
Demostración de tipos de datos básicos en Python.
Imprime el tipo de: una cadena (str), un entero (int) y un flotante (float).
"""

# type(obj) devuelve el tipo del objeto pasado como argumento.
print(type("a"))   # Resultado esperado: <class 'str'> (cadena)
print(type(2))      # Resultado esperado: <class 'int'> (entero)
print(type(2.5))    # Resultado esperado: <class 'float'> (flotante)
