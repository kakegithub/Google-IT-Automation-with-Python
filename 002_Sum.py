"""
Programa de ejemplo: suma dos números y muestra el resultado.
Nota: aquí se usa la variable 'sum' como nombre de variable, lo cual sombrea la
función incorporada sum() de Python. Se mantiene así para conservar el código original.
"""

# Definimos los operandos (números a sumar)
num1 = 1
num2 = 2

# Calculamos la suma de ambos valores y la guardamos en 'sum'
sum = num1 + num2

# Mostramos el resultado en consola.
# La función print acepta varios argumentos y los separa con un espacio por defecto.
print("The sum of the numbers is:", sum)
